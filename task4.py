# Używając funkcji ord() , znajdź i wyświetl kody liczbowe ASCII dla
#pierwszej litery Twojego imienia (wielką i małą literą)
first_letter_upper = ord("H")
first_letter_lower = ord("h")
print(f"Kod ASCII dla wielkiej litery H to: {first_letter_upper}")
print(f"Kod ASCII dla małej litery h to: {first_letter_lower}")